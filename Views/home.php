        <h4>Dans des temps immémoriaux....une définition du JDR....</h4>
        <p>
            Un jeu de rôle ou abrégé JdR (de l'anglais roleplaying game ; abrégé RPG),
            aussi appelé jeu de rôle sur table ou jeu de rôle papier pour le différencier
            des autres formes de jeu de rôle, est un jeu de société dans lequel les participants
            conçoivent ensemble une fiction par l’interprétation de rôles et par la narration,
            dans le cadre de contraintes de jeu qu’ils s’imposent.

            Ce jeu, dans sa forme traditionnelle, rassemble les joueurs
            durant la partie autour d'une table, avec communément pour accessoires
            des dés et feuilles de papier. Ce jeu rassemble aussi des joueurs pour des parties sur Internet,
            et il a inspiré d'autres formes de jeux de rôle tels que le grandeur nature et certains jeux vidéo.
            <em>(source Wikipédia).</em>
        </p>
        <h4>Et soudain... rôliste...</h4>
        <p>
            Les joueurs de jeu de rôle sont appelés rôlistes, néologisme inventé
            par Pierre Rosenthal dans les années 1980. En 2016, le mot figure sur le dictionnaire Larousse en ligne2.
            Des joueurs se réunissent pour une période de 3 h à 6 h. Ils créent des personnages qui seront les avatars à travers
            desquelles ils interagiront avec l'univers imaginaire décrit par le maître du jeu . Les participants œuvrent
            ensemble pour faire avancer le scénario.
            Le but est de nourrir son esprit d'imagination tout en partageant un bon moment.
            <em>(source Wikipédia).</em>
        </p>

        <h4>Et c'est ainsi.... qu'on définit un maître du jeu?</h4>
        <p>
            Le maître du jeu créé un univers et met en scène des rôlistes dans une intrigue. C'est à la fois le narrateur de l'histoire
            et parfois il incarne les personnages non joueur (P.N.J) afin de nourrir son univers avec des dialogues.
            Il définie les règles du jeu, les compétences des avatars et des PNJ, afin de maintenir la cohérence de son univers.
            <em>(source Wikipédia).</em>
        </p>
